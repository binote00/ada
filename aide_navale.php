<h2>Spécificités de la guerre navale</h2>

<h3>Actions navales</h3>

<p><u>Appareiller</u>
<br>Déplacer vos navires entre les lieux maritimes, les ports ou les plages.
<br>Veillez à posséder une quantité suffisante de carburant!</p>

<p><u>Ecran de fumée (camoufler les navires à quai)</u>
<br>Annuler l'effet d'une reconnaissance ennemie.</p>

<p><u>Effectuer des manoeuvres d'entrainement</u>
<br>Uniquement accessible dans votre port d'attache, permet d'augmenter l'expérience de vos navires jusqu'à un maximum de 50.</p>

<p><u>Poser des filets anti-torpilles</u>
<br>Immobilise le navire en le protégeant contre les attaques à la torpille.</p>

<p><u>Quadriller la zone maritime</u>
<br>Action similaire à la revendication terrestre; les troupes de votre faction combattront avec l´avantage du terrain.</p>

<p><u>Retirer les filets anti-torpilles</u>
<br>Permet à votre navire de se déplacer à nouveau, ne le protégeant plus contre les attaques à la torpille.</p>

<p><u>Saborder votre dernier navire et attendre les sauveteurs</u>
<br>Action de désespoir vous permettant de rejoindre votre port d'attache, si votre flotte a été anéantie.</p>

<p><u>Surface / En Plongée</u>
<br>Actions réservées aux sous-marins.
<br>En surface, la visibilité du sous-marin augmente ainsi que sa vitesse.
<br>En plongée, la visibilité du sous-marin diminue ainsi que sa vitesse, mais la distance à laquelle il peut engager l'ennemi augmente.</p>

<h3>Nouvelles formations tactiques navales</h3>

<p><u>Formation dispersée</u>
<br>Une attaquee ennemie ne peut cibler qu'un seul navire à la fois (perte maximale par attaque : 1 navire)
<br>Aucun effet contre les attaques aériennes</p>

<p><u>Formation d'escorte (équivalent de la ligne en terrestre)</u>
<br>Les escorteurs (destroyer, frégate, corvette) font écran, empêchant toute attaque de cargos, tankers et/ou porte-avions alliés
<br>Aucun effet contre les attaques aériennes ou sous-marines
<br>Fonctionne comme un appui lors des attaques à la torpille en surface</p>

<p><u>Formation d'évasion</u>
<br>Ecran de fumée automatique en cas d'attaque subie (éviter toute attaque en surface, sauf si ennemi plus rapide ou présence de radar)
<br>Riposte impossible
<br>Aucun effet contre les attaques aériennes ou sous-marines</p>

<p><u>Formation d'appui (équivalent de l'appui en terrestre)</u>
<br>Les armes de soutien des navires riposteront à toute attaque sur des unités alliées sur le même lieu (à condition d'avoir la portée suffisante)</p>

<p><u>Formation ASM</u>
<br>Toute attaque sous-marine sera suivie d'une riposte ASM, à condition que les navires possèdent des charges de profondeur</p>

<h3>Armement</h3>

<br>Les navires possèdent différents types d'armement

<p><u>- ASM / DCA</u>
<br>Grenades sous-marines ASM pour les navires possédant des charges de profondeur, dans le cas contraire il s'agit d'une DCA complémentaire spécialisée dans les attaques à basse altitude (0-1000m)</p>
<p><u>- Principal</u>
<br>Armement principal de la plupart des navires, sauf pour les porte-avions où il s'agit d'une DCA complémentaire spécialisée dans les attaques à moyenne altitude (1000-4000m)</p>
<p><u>- Mines</u>
<br>Armement passif permettant de miner une zone (voir description du mouilleur de mines).</p>
<p><u>- Torpilles</u>
<br>Armement nécessaire au torpillage, ne peut évidemment cibler que d'autres navires.</p>
<p><u>- DCA</u>
<br>DCA principale du navire, intervenant aux altitudes hautes et moyennes (4000-6000m)</p>


<h3>Actions des flottilles</h3>

<p><u>- Reco</u>
<br>Permet de détecter la présence éventuelle de navires de surface ennemis dans la zone maritime où se trouve votre flotte
<br>Localise les navires pour une éventuelle attaque aérienne</p>

<p><u>- Traque ASM</u>
<br>Permet de décecter la présence éventuelle de sous-marins ennemis dans la zone maritime où se trouve votre flotte
<br>Si un ou plusieurs sous-marins sont détectés, un grenadage automatique est opéré (à condition d'avoir des munitions suffisantes !)</p>

<p><u>- Attaquer</u>
<br>Fonctionne comme une attaque terrestre. Les formations adoptées et les éventuelles couvertures mises en place peuvent déclencher des ripostes, des esquives ou des fuites.</p>

<p><u>- Torpiller</u>
<br>Permet d'utiliser les torpilles du navire (ou du sous-marin) pour attaquer un navire adverse.
<br>Un torpillage en surface pourra déclencher une riposte ennemie, en fonction des unités présentes, des formations adoptées et des couvertures mises en place.
<br>Un torpillage en plongée (sous-marin uniquement) pourra déclencher une riposte ASM (grenadage), en fonction des unités présentes, des formations adoptées et des couvertures mises en place.</p>

<p><u>- Tirer</u>
<br>Fonctionne comme le bombardement terrestre, à savoir une salve réalisée à l'aide de l'arme de soutien, à la portée maximale.
<br>Les formations adoptées et les éventuelles couvertures mises en place peuvent déclencher des ripostes de type "contre-batterie".</p>

<p><u>- Bombarder</u>
<br>Fonctionne comme la destruction terrestre, à savoir une salve réalisée à l'aide de l'arme de soutien, en vue de détruire les infrastructures ennemies.</p>
<br>Cette option n'est disponible que sur un zone portuaire.

<p><u>- Miner</u>
<br>Une fois posées, les mines ont une chance d'endommager tous les navires passant par cette zone.
<br>Cette option n'est disponible que sur un zone portuaire, une zone propice au débarquement ou un détroit.</p>

<p><u>- Déminer</u>
<br>Les dragueurs de mines (et tout autre navire avec la caractéristique "démineur") peuvent retirer les mines d'une zone.</p>


<h3>Unités navales</h3>

<p><u>- Le cargo de haute mer</u>
<br>Identique au cargo, ce dernier possède cependant une autonomie accrue et une plus grande résistance lui permettant de naviguer sur les longues distances en eau profonde de l'océan.</p>

<p><u>- Le dragueur de mines</u>
<br>Ce petit navire permet de déminer une zone maritime, comme le ferait une unité de génie pour une zone terrestre.
<br>Les dragueurs de mines (et tout autre navire avec la caractéristique "démineur") peuvent retirer les mines d'une zone.</p>

<p><u>- Le navire de débarquement</u>
<br>Ce petit navire permet de transporter des troupes ('ressource' disponible dans les dépôts) pour ensuite les débarquer sur un lieu tenu par l'ennemi.
<br>Une fois débarquées, ces troupes effectueront des actions de sabotage et des attaques sur les infrastructures ennemies. Elles peuvent cependant être contrées par des garnisons ou des unités de police.
<br>Pour rappel : les unités de transport aérien peuvent effectuer le même type d'action via des parachutages.</p>

<p><u>- Le mouilleur de mines</u>
<br>Ce petit navire permet de miner certaines zones maritimes, soit l'entrée d'un port, soit les abords d'une plage, soit un détroit ou un canal.
<br>Le mouilleur de mines ne peut miner une zone que si aucune unité ennemies n'est présente sur cette zone en même temps que lui.
<br>Une fois posées, les mines ont une chance d'endommager tous les navires passant par cette zone.
<br>Les dragueurs de mines (et tout autre navire avec la caractéristique "démineur") peuvent retirer les mines d'une zone.</p>

<p><br>- Les lieux maritimes ne comportent qu'une zone, où tous les navires se retrouvent et peuvent s'engager si leur vitesse et leur armement le leur permet.
<br>- Les navires importants possèdent une "barre de vie" (en %) visible dans le menu principal de gestion des flottes. Chaque dégât reçu fait baisser cette valeur. Une fois arrivée à 0%, le navire est coulé.
<br>- Des formations de combat spécifiques à la guerre navale remplacement les positions tactiques terrestres.
<br>- Le téléphone noir fonction de la même manière, mis à part qu'il est évidemment impossible de demander un transport ou un ravitaillement ferroviaire depuis votre unité navale.</p>
