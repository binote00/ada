<h1>4- Cibles au sol</h1>
Comme vu dans les missions, les unités aériennes peuvent attaquer à l'aide de bombes, de roquettes, de torpilles ou d'armes de bord (canon, mitrailleuses) différentes cibles au sol, infrastructures, aérodromes, troupes ou navires.
<h2>L'identification des cibles</h2>
Pour pouvoir être attaquée, une cible doit être visible. Pour ce faire, une mission de reconnaissance préalable est nécessaire. Elle peut être effectuée par une unité de reconnaissance aérienne ou une unité terrestre ou navale.
<br>Une cible statique (comme une infrastructure) nécessite une reconnaissance stratégique, tandis qu'une cible mobile (une unité terrestre ou navale) nécessite une reconnaissance tactique.
<h2>L'attaque</h2>
Une fois la mission de reconnaissance préalable effectuée, les cibles peuvent être attaquées ou bombardées une fois l'avion arrivé sur l'objectif. C'est alors que la DCA (défense contre avions) intervient.
<p>Une fois la DCA neutralisée ou évitée, la cible subira votre attaque. Les dégâts occasionnés permettront de détruire la cible ou de l'endommager, occasionnant divers effets sur le déroulement du jeu (Reportez vous à la section terrestre et aux fiches en jeu concernant les particularités des véhicules et des infrastructures).
<br>Il vous sera possible d'effectuer plusieurs passages si vous emportez suffisamment de munitions ou de bombes, mais attention, plus vous en effectuerez plus la DCA aura facile d'ajuster son tir sur votre avion.</p>
<h2>La DCA</h2>
<p>Il existe 3 types de DCA dans le jeu : la DCA de site, la DCA de protection et la DCA rapprochée.</p>
<p>- La DCA de site (1e rideau) intervient lorsqu'un avion passe au-dessus d'un lieu terrestre donné (il n'y a donc pas de DCA de site en mer). La puissance de la DCA de site dépend, de base, de la valeur stratégique du lieu. Cette DCA peut être améliorée ou réparée par l'EM de front et elle se régénère automatiquement dès que le site n'est plus sous attaque. Cette DCA ne peut être détruite que si vous attaquez une batterie de DCA fixe, à l'exception de celle de l'aérodrome. La DCA de la caserne, de l'usine, de la gare, du pont, etc... Cette DCA peut être évitée au-dessus d'un lieu contrôlé par votre faction si votre avion est équipé d'une radio améliorée ou d'une radio longue-portée.</p>
<p>- La DCA de protection intervient pour défendre toute cible attaquée sur le lieu (terrestre ou maritime). Dans le cas d'une infrastructure (usine, port, gare, radar, caserne, etc...), il s'agit de la DCA de site, sauf dans le cas d'une attaque d'aérodrome (voir plus bas). Dans le cas d'une cible mobile (navire, bataillon, train, etc...), il s'agit des unités de DCA alliées de la cible présentes sur les lieux. Ces unités peuvent être identifiées via les fiches présentes en jeu (Couverture DCA : Oui ou Non). Un maximum de 2 DCA peuvent tirer lors d'une attaque donnée, la priorité étant donnée aux plus expérimentées.</p>
<p>- La DCA rapprochée intervient pour défendre une cible précise, et elle peut être différente d'une cible à l'autre. Sa valeur est toujours fixe, pré-déterminée par la cible elle-même (voir fiches infrastructures et unités en jeu). Cette DCA ne peut être détruite individuellement, elle fait partie de la cible et ripostera TOUJOURS si la cible est attaquée. Une seule exception cependant, si la cible est une unité blitzkrieg contrôlée par un joueur et que cette unité n'a plus de munitions, un message indiquera que la DCA reste silencieuse.</p>
<p>- La DCA d'aérodrome est spéciale, dans le sens où elle ne défend QUE l'aérodrome, mais également TOUT ce qui se trouve sur l'aérodrome. Cette DCA protégera la piste, la tour de contrôle, les hangars, les avions au sol, etc... Cette DCA est la seule qui puisse être améliorée par les joueurs, et elle ne peut être détruite que si vous attaquez une batterie de DCA de l'aérodrome. Cette DCA est du niveau DCA de protection, détruire toutes les batteries de DCA d'aérodrome ne vous immunisera donc pas contre la DCA de site ou la DCA rapprochée. Dans le cas d'un aérodrome occupé par plusieurs unités, la DCA d'aérodrome sera équivalente à la somme des DCA de toutes les unités.</p>
