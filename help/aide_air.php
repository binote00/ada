<h2>Préparation de la mission</h2>
Avant de pouvoir décoller, il vous faut bien entendu un avion ! Au grade le plus bas, l'avion vous est imposé par votre chef d'escadrille. 
<br>A partir du grade de <i>Sergent-Chef (ou Unterfeldwebel ou Sergeant)</i>, vous avez la possibilité de choisir l'avion que vous utiliserez, parmi 3 disponibles.
<br>Les avions disponibles dépendent de l'unité dans laquelle vous vous trouvez, ainsi que de l'état du matériel.
<br><br>Notez que l'objectif est également choisi par le chef d'escadrille. Lorsque vous atteignez le grade de <i>Capitaine (ou Hauptmann ou Flying Lieutenant)</i>, vous pouvez également choisir votre objectif.
<h2>Début de la mission</h2>
Toutes les missions débutent par le décollage, moment crucial précédent le vol à proprement parlé. La météo, ainsi qu'un éventuel problème technique, peuvent mettre un terme prématuré à votre aventure.
<br>
<h2>Missions d'entrainement</h2>
Ces missions permettent au pilote débutant de progresser dans diverses compétences, sans courir le danger d'être attaqué par l'ennemi.
<br>En contrepartie, ces missions n'apportent aucune réputation, ni aucun avancement.
<h2>Missions d'appui</h2>
Les missions d'appui ont pour objectif la destruction des unités terrestres ennemies (infanterie, artillerie, blindés, camions, trains, etc...)
<p class='lead'>La tactique</p>
Durant ces missions, il s'agira d'utiliser les armes de bord de l'avion (mitrailleuses, canons), et non des bombes. Lorsqu'elles sont disponibles, les rockets peuvent être utilisées.
<br>Remarque : Les cibles de niveau >Insignifiant< ne suffisent pas pour valider une mission de faction.
<p class='lead'>La stratégie</p>
Afin de pouvoir attaquer les cibles de première importance, il est nécessaire que l'objectif soit identifié par une mission de reconnaissance <b>tactique</b>.
<br>Comme toute mission d'attaque ou de bombardement, une escorte placée sur l'objectif par votre faction est nécessaire si l'ennemi couvre l'objectif avec une patrouille défensive. Sans cela, la cible ne pourra pas être attaquée.
<p class='lead'>Le choix de l'avion</p>
Les missions d'appui nécessitent une capacité de destruction importante et donc un armement offensif important, de préférence des rockets ou des canons de gros calibre.
<br>Les objectifs des missions d'attaque sont parfois défendus par de la D.C.A. Il est préférable que l'avion possède une bonne robustesse et un blindage pour encaisser les éventuels tirs ennemis.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Une météo clémente vous permettra de mieux détecter l'objectif tout en vous permettant de viser plus facilement votre cible.
<br>En contrepartie, une météo clémente donnera plus de chance à la D.C.A de vous repérer et de vous abattre.
<h2>Missions d'attaque au sol</h2>
Les missions d'attaque au sol ont pour objectif la destruction des installations fixes au sol, comme des aérodromes, des gares, des usines, des ponts, des casernes, etc...
<p class='lead'>La tactique</p>
Durant ces missions, il s'agira d'utiliser les armes de bord de l'avion (mitrailleuses, canons), et non des bombes. Lorsqu'elles sont disponibles, les rockets peuvent être utilisées.
<br>Remarque : Les cibles de niveau >Insignifiant< ne suffisent pas pour valider une mission de faction.
<p class='lead'>La stratégie</p>
Afin de pouvoir attaquer des cibles de première importance, il est nécessaire que l'objectif soit identifié par une mission de reconnaissance <b>stratégique</b>.
<br>Comme toute mission d'attaque ou de bombardement, une escorte placée sur l'objectif par votre faction est nécessaire si l'ennemi couvre l'objectif avec une patrouille défensive. Sans cela, la cible ne pourra pas être attaquée.
<br>En tant que simple pilote, vous n'avez pas à vous soucier de cela, c'est la responsabilité de votre chef d'escadrille ou de l'état-major.
<p class='lead'>Le choix de l'avion</p>
Les missions d'appui nécessitent une capacité de destruction importante et donc un armement offensif important, de préférence des rockets ou des canons de gros calibre.
<br>Les objectifs des missions d'attaque sont parfois défendus par de la D.C.A. Il est préférable que l'avion possède une bonne robustesse et un blindage pour encaisser les éventuels tirs ennemis.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Une météo clémente vous permettra de mieux détecter l'objectif tout en vous permettant de viser plus facilement votre cible.
<br>En contrepartie, une météo clémente donnera plus de chance à la D.C.A de vous repérer et de vous abattre.
<h2>Missions de bombardement naval</h2>
Les missions de bombardement naval sont identiques aux missions de bombardement tactique, sauf pour leur objectif qui sont des navires ennemis.
<br>Ne choisissez pas cette mission si votre avion ne transporte pas de bombes!
<h2>Missions de bombardement tactique</h2>
Les missions de bombardement tactique sont identiques aux missions d'appui, sauf qu'elles nécessitent l'utilisation de bombes.
<br>Ne choisissez pas cette mission si votre avion ne transporte pas de bombes!
<h2>Missions de bombardement stratégique</h2>
Les missions de bombardement stratégique sont identiques aux missions d'attaque au sol, sauf qu'elles nécessitent l'utilisation de bombes.
<br>Ces missions sont réservées aux unités de Bombardement.
<br>Ne choisissez pas cette mission si votre avion ne transporte pas de bombes!
<br>Pour les missions de bombardement de nuit, un bon niveau de navigation et un membre d'équipage expérimenté sont conseillés.
<br>De plus, il est conseillé de faire précéder tout bombardement de nuit par un marquage de cible effectué par une unité de reconnaissance, sous peine de ne pas pouvoir détecter la cible.
<h2>Missions de chasse libre</h2>
Les missions de chasse libre sont la panacée pour l'as visant un total de victoires élevé, ou certaines décorations prestigieuses.
<br>Ces missions sont réservées aux unités de Chasse.
<br>Durant ces missions, le pilote peut engager le combat à loisir contre les cibles de son choix, et rentrer à sa base quand bon lui semble.
<br>En contrepartie, ce genre de mission n'apporte pas grand chose en terme d'avancement, pas plus qu'il n'aide votre faction au niveau de la campagne.
<p class='lead'>Le choix de l'avion</p>
Les missions de chasse nécessitent un avion rapide, puissant et manoeuvrable.
<h2>Missions d'escorte</h2>
Les missions d'escorte consistent à escorter une formation d'avions amis jusqu'à leur objectif.
<br>Ces missions sont réservées aux unités de Chasse.
<p class='lead'>La tactique</p>
Durant l'escorte, l'objectif est de défendre les bombardiers à tout prix. Le résultat de la mission dépendra du nombre de bombardiers préservés de l'ennemi.
<br>Choisir de ne pas protéger les bombardiers mènera à l'échec de la mission.
<p class='lead'>La stratégie</p>
Une escorte placée sur l'objectif permettra aux bombardiers et avions d'attaque de bénéficier de l'aide des avions en escorte lors de leur mission. De plus, une escorte est nécessaire si l'ennemi couvre l'objectif avec une patrouille défensive. Sans cela, la cible ne pourra pas être attaquée.
Lorsque votre avion réussit une mission d'escorte, il est assigné à cette cible au tableau de mission de l'escadrille. Vous ne pouvez être assigné qu'à une seule cible à la fois, peu importe la mission.
<p class='lead'>Le choix de l'avion</p>
Un avion possédant une autonomie importante et un bon armement sont conseillés.
<h2>Missions d'interception</h2>
Les missions d'interception consistent à décoller rapidement de votre base et attaquer la formation ennemie détectée.
<br>Ces missions sont réservées aux unités de Chasse. 
<br>Les unités de chasseurs légers sont les seules unités pouvant effectuer des interceptions de jour, tandis que les chasseurs lourds sont les seules unités pouvant effectuer des interceptions de nuit.
<br>L'objectif est de détruire le plus d'avions ennemis possible.
<p class='lead'>La stratégie</p>
Lorsque votre avion réussit une mission d'interception, il est assigné à la couverture de votre aérodrome au tableau de mission de l'escadrille. Vous ne pouvez être assigné qu'à une seule cible à la fois, peu importe la mission.
<p class='lead'>Le choix de l'avion</p>
Un avion possédant une vitesse ascensionnelle élevée est conseillé.
<h2>Missions de harcèlement</h2>
Les missions de harcèlement sont des missions de nuit permettant de réduire la couverture ennemie sur une zone donnée, tout en offrant la possibilité d'attaquer les installations de l'ennemi.
<br>Ces missions sont réservées aux unités de Chasse Lourde.
<p class='lead'>La stratégie</p>
Durant ces missions, le pilote engage le combat contre les chasseurs de nuit ennemis, afin de diminuer la force de la couverture ennemie et permettre aux unités de reconnaissance et de bombardement d'effectuer leurs missions.
<br>L'attaque d'objectifs au sol s'effectue de la même manière que lors d'une mission d'attaque au sol, sauf qu'elle se déroule de nuit. Une reconnaissance stratégique préalable est nécessaire.
<p class='lead'>Le choix de l'avion</p>
Les missions de harcèlement nécessitent un avion rapide, puissant et manoeuvrable. L'armement de bord doit également être conséquent. Une bonne autonomie est conseillée.
<h2>Missions de marquage de cible</h2>
Ces missions consistent à éclairer un objectif de nuit, facilitant le repérage et la visée des unités de bombardement.
<br>Ces missions sont réservées aux unités de Reconnaissance.
<p class='lead'>Le choix de l'avion</p>
Un avion rapide ou discret et possédant une bonne autonomie. La stabilité est un atout pour la précision du largage.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Si vous ne pouvez pas détecter votre cible, vous devrez annuler votre mission.
<h2>Missions de mouillage de Mines</h2>
Ces missions consistent à mouiller des mines sur une zone maritime.
<p class='lead'>La stratégie</p>
Les mines maritimes ont une chance d'endommager tout navire traversant la zone où les mines sont posées.
<p class='lead'>Le choix de l'avion</p>
Un avion pouvant transporter des mines maritimes est un prérequis. Il vaut mieux qu'il dispose d'une bonne autonomie et éventuellement de bonnes capacités défensives si l'objectif devait se situer en zone dangereuse.
<h2>Missions de patrouille ASM</h2>
Ces missions consistent à patrouiller dans une zone maritime à la recherche d'un éventuel sous-marin ennemi.
<br>Si un sous-marin est détecté, le but de la mission est alors de le couler, en utilisant les armes de bord s'il est en surface ou des charges de profondeur s'il est en immersion.
<p class='lead'>Le choix de l'avion</p>
Il vaut mieux choisir un avion équipé d'un radar, équipement indispensable à la détection de sous-marins en plongée. Les charges de profondeur (ou mines) sont également très utiles.
<br>Un avion polyvalent pourra répondre efficacement à tous les types de menaces. Il vaut mieux qu'il emporte des bombes pour faire face aux navires et qu'il soit équipé de canons ou de mitrailleuses pour un éventuel combat aérien.
<h2>Missions de patrouille défensive</h2>
Ces missions nécessitent que vous patrouillez entre deux points en interceptant tous les avions ennemis détectés.
<br>Ces missions sont réservées aux unités de Chasse.
<br>A la différence des missions de chasse libre, suivre le parcours imposé détermine le succès de la mission.
<br>En contrepartie, les patrouilles vous récompensent plus généreusement en terme d'avancement que les missions de chasse libre.
<p class='lead'>La stratégie</p>
L'altitude à laquelle vous placez votre patrouille déterminera à quel niveau d'altitude vos chasseurs pourront intercepter les avions ennemis. Ils pourront intercepter tout ennemi approchant de 1000m plus haut à 3000m plus bas, par temps clair.
<br>Une patrouille défensive placée sur un lieu permettra aux avions alliés effectuant une mission vers ce lieu de bénéficier de l'aide des avions en patrouille lors de leur mission. De plus, une patrouille défensive empêchera toute attaque ennemie sur ce lieu tant qu'une escorte ennemie ne sera pas placée.
<br>Une patrouille défensive placée sur une base de bombardier allié permettra à ces bombardiers de bénéficier d'une protection sur le chemin du retour.
<br>Lorsque votre avion réussit une mission de patrouille défensive, il est assigné à la couverture de cette cible au tableau de mission de l'escadrille. Vous ne pouvez être assigné qu'à une seule cible à la fois, peu importe la mission.
<p class='lead'>Le choix de l'avion</p>
Comme pour les missions de chasse, les patrouilles nécessitent un avion rapide, puissant et manoeuvrable.
<h2>Missions de reconnaissance stratégique</h2>
Ces missions consistent à effectuer une reconnaissance d'une région. Les renseignements rapportés détermineront le succès de votre mission.
<br>Ces missions sont réservées aux unités de Reconnaissance.
<br>Ces missions sont indispensables à l'identification des cibles stratégiques d'importance, sans quoi ces dernières ne peuvent être attaquées.
<br>Ces missions sont un prérequis obligatoire aux missions de bombardement stratégique, de harcèlement et d'attaque au sol.
<p class='lead'>La stratégie</p>
L'état-major ennemi peut annuler l'effet d'une reconnaissance stratégique en camouflant les infrastructures. Il est donc utile de vérifier régulièrement sur la carte l'état de l'objectif.
<br>Le coût en CT du camouflage d'infrastructures est augmenté par le nombre d'avions placés en observation sur le lieu. Les unités de reconnaissance peuvent placer des avions en observation.
<p class='lead'>Le choix de l'avion</p>
Un avion rapide et possédant un plafond important est conseillé, afin d'éviter au maximum la chasse ennemie.
<br>Certains avions sont spécialisés dans la reconnaissance. Lorsque vous aurez accès à votre avion personnel, vous pourrez l'équiper de caméras, ce qui augmentera vos chances de succès.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Si vous ne pouvez pas détecter votre cible, vous devrez annuler votre mission.
<h2>Missions de reconnaissance tactique</h2>
Ces missions consistent à effectuer une reconnaissance d'un objectif mobile (le plus souvent une unité ennemie).
<br>Ces missions sont un prérequis obligatoire aux missions d'appui, de bombardement tactique, de bombardement naval ou de torpillage.
<p class='lead'>La stratégie</p>
Les unités terrestres ou navales peuvent annuler l'effet d'une reconnaissance en se camouflant ou en se déplaçant. Il est donc utile de vérifier régulièrement sur la carte les positions de l'ennemi.
<p class='lead'>Le choix de l'avion</p>
Un avion rapide et possédant un plafond important est conseillé, afin d'éviter au maximum la chasse ennemie. Une bonne robustesse peut aider face à la D.C.A
<br>Bien que cette mission puisse être réalisée avec bon nombre d'avions, les avions de reconnaissance bénéficient de manoeuvres spéciales d'évitement ainsi que d'un bonus supplémentaire lors du repérage des cibles.
<br>De plus, lorsque vous aurez accès à votre avion personnel vous pourrez l'équiper de caméras, ce qui augmentera vos chances de succès.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Si vous ne pouvez pas détecter votre cible, vous devrez annuler votre mission.
<h2>Missions de sauvetage</h2>
Ces missions consistent à récupérer un pilote (joueur) abattu en territoire ennemi.
<br>Ces missions sont réservées aux unités de Reconnaissance.
<p class='lead'>Le choix de l'avion</p>
Un avion discret, maniable, capable de se poser en terrain difficile et possédant une bonne autonomie.
<p class='lead'>La météo</p>
La météo <i>sur l'objectif</i> a une grande importance pour ce genre de mission. 
<br>Si vous ne pouvez pas détecter votre cible, vous devrez annuler votre mission.
<h2>Missions de torpillage</h2>
Les missions de torpillage sont identiques aux missions de bombardement naval, sauf qu'elles nécessitent l'emploi d'une torpille.
<br>Ne choisissez pas cette mission si votre avion ne transporte pas de torpille!
<br>
<h2>Missions spéciales</h2>
Les missions historiques et de front sont des missions bonus s'ajoutant aux missions classiques.
<ul><li>La mission historique représente un évènement réel de la seconde guerre mondiale, adapté pour le jeu.</li>
<li>La mission de front est définie par l'Etat-Major de votre nation, et est accessible à toutes les escadrilles de la nation présentes sur ce front.</li></ul>
<h4><small><b>Attention que lors de ces missions, c'est à vous de vérifier que l'objectif est accessible et approprié!</b></h4></small>
