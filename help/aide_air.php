<h1>3- Combat aérien</h1>
Dans le jeu, le combat aérien se décompose en plusieurs phases.
<ul>
<li> La détection</li>
<li> L'approche</li>
<li> La manoeuvre</li>
<li> Le tir</li>
</ul>
<h2>La détection</h2>
Lorsqu'une rencontre a lieu, chaque formation a la possibilité de détecter l'autre. Les conditions météo, la taille des avions, le camouflage utilisé, mais également divers équipements des avions respectifs modifient les capacités de détection des pilotes.
<br>La formation détectant son adversaire en premier gagne l'initiative lors des combats. Dans certaines situations, cela peut également donner un avantage de surprise à la formation attaquante.
<h2>L'approche</h2>
Lors de cette phase, les adversaires décident d'engager ou non le combat. L'atout principal lors de cette phase est bien entendu la vitesse de l'avion, mais également son altitude ou les conditions météo.
<h2>La manoeuvre</h2>
Cette phase permet aux protagonistes de mesurer véritablement leur talent de pilote. Par soucis de simplification, les différentes possibilités ont été regroupées en catégories : basées sur la vitesse, la manoeuvre ou l'acrobatie.
<br>Les choix du menu de combat vous proposent différentes tactiques offensives et défensives, en fonction de l'expérience Tactique de votre pilote.
<h2>Le tir</h2>
<h4><small>La réussite du tir dépend de nombreux facteurs. L'arme utilisée, la distance, l'angle d'attaque, l'altitude, les conditions météo, et bien entendu l'habileté du pilote.</h4></small>
Plus la distance sera courte et plus l'angle sera petit, plus vous aurez de chances de toucher votre cible. Les armes et leurs munitions ont également des données ballistiques différentes.
<br>Votre choix de tir influencera également grandement le résultat. Une courte rafale sera plus précise qu'une longue, mais elle aura également moins de chances de faire beaucoup de dégâts, tout en consommant moins de munitions.
<br>Vos armes risquent également de s'enrayer en plein combat. L'enrayage dépend de l'arme utilisée, des conditions météo et de l'altitude. Un problème mécanique, mais également la température peuvent être la cause d'un enrayage.
<br>Vous pouvez rendre visite à l'armurier de l'escadrille, qui vous prodiguera des conseils.
