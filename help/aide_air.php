<h1>1- Avions</h1>
<div class='alert alert-info'>Il existe de nombreux types d'avions dans le jeu (attaque, bombardier, bombardier lourd, chasseur, chasseur lourd, chasseur-bombardier, avion embarqué, entrainement, patrouille maritime, reconnaissance, transport)
<br>Chaque nation dispose de modèles spécifiques, déclinés en différentes versions. Chacune d'entre elle possède des caractéristiques propres.</div>
<h2>Caractéristiques</h2>
<h3>Autonomie</h3>
La distance maximale (en km) que l'avion peut parcourir.
<br>La distance réellement franchissable peut être différente de l'autonomie théorique. Elle est notamment influencée par la vitesse de l'avion, son altitude, la météo, les modifications de masse et certains évènements de mission.
<h3>Plafond</h3>
L'altitude maximale (en mètres) que l'avion peut atteindre.
<br>Le plafond pratique peut être différent du plafond théorique, notamment en fonction de la puissance délivrée par le moteur de l'avion ainsi que par les éventuels dégâts reçus.
<br>Notez que les performances des avions varient fortement avec l'altitude, référez-vous aux fiches ou consultez le mécano de votre escadrille pour plus d'informations.
<h3>Puissance</h3>
Délivrée par le moteur, elle permet à l'avion de se maintenir en vol ainsi que d'effectuer des accélérations.
<br>La puissance dépend de plusieurs facteurs, le principal étant la puissance nominale du moteur et le régime choisi via la manette des gaz.
<br>L'altitude et les dégâts reçus sont également des facteurs pouvant modifier fortement la puissance.
<br>Consultez le mécano de votre escadrille pour obtenir plus d'informations sur les moteurs.
<h3>Rayon de virage</h3>
La capacité de l'avion à virer court. Plus un avion possède un rayon de virage court, plus il sera capable de dominer son adversaire en combat tournoyant et plus il sera à l'aise lors de certaines acrobaties.
<h3>Robustesse</h3>
La capacité de l'avion à encaisser les dégâts. Si la robustesse tombe à 0, l'avion est abattu.
<h3>Taux de roulis</h3>
La capacité de l'avion à exécuter rapidement une manoeuvre sur l'axe de roulis. Caractéristique prisée des chasseurs et autres avions maniables, notamment lors de l'exécution des tonneaux.
<h3>Vitesse ascensionnelle</h3>
La capacité de l'avion à gagner rapidement de l'altitude.
<br>La vitesse ascensionnelle dépend fortement de la puissance moteur, du régime choisi et de l'altitude.
<h3>Vitesse en palier</h3>
La capacité de l'avion à se déplacer plus rapidement en vol horizontal.
<br>La vitesse maximale varie avec la puissance moteur, l'altitude, la météo et les dégâts reçus.
<h3>Vitesse en piqué</h3>
La capacité de l'avion à se déplacer plus rapidement en piqué. 
<br>La vitesse maximale en piqué est limitée par la résistance de la cellule.
