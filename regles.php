<div id='tab_credit'>
<h1>5- Le Crédit Temps <img src='images/CT24.png'></h1>
<div class='alert alert-info'>Pour effectuer différentes actions dans le jeu, notamment les missions, vous aurez besoin de Crédit Temps.</div> 
Les Crédits Temps s'obtiennent automatiquement. Chaque jour vous en recevez un certain nombre (24 pour le pilote et 50 pour l'officier),lors de votre première connexion de la journée entre 7h et minuit.
<br> Attention cependant que votre capital point est réinitialisé chaque jour. Il n'est pas possible d'accumuler des points d'un jour à l'autre, les points inutilisés sont perdus.
<br><br> Certaines actions coûtent plus ou moins de points (ou en rapportent parfois), le coût étant indiqué dans les différents menus par l'icône <img src='images/CT0.png'>.
</div>
