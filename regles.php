<h1>1- Le Contexte</h1>
<h4><small>L'Aube des Aigles est un jeu se déroulant dans le cadre de la seconde guerre mondiale (1939-1945).</h4></small>
Le jeu propose de revivre les batailles de la seconde guerre mondiale au jour le jour, à travers l'incarnation de différents acteurs de cette guerre à différents niveaux de commandement. 
<br>Du simple pilote au chef d'escadrille, de l'officier terrestre commandant un bataillon au général d'armée ou encore de l'officier de marine commandant une flottille à l'amiral de la flotte. Tout cela dans un gameplay privilégiant la coopération multi-joueurs. 
<br>De la grande stratégie d'état-major à la tactique sur le terrain, l'Aube des Aigles propose à chaque joueur d'influencer le cours des évènements d'un des plus grands conflits de notre histoire.
<br>De l'Europe au Pacifique en passant par l'Afrique du Nord, la Méditerranée, le front de l'Est ou l'Atlantique, aucun front important n'a été oublié.  
<br>Que ce soit en terme de géographie, d'ordre de bataille des unités, de matériel, de déplacements et mutations ou encore de personnages historiques, le jeu tend à respecter l'histoire.
<br>Les joueurs ont le contrôle de tout ce qui concerne les prises de décision sur le terrain, l'action, le déroulement de la bataille et ses conséquences. En bref, ce qui détermine la victoire ou la défaite !
</div>
