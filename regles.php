<div id="tab_compet">
<h1>2- La victoire</h1>
En terme de compétition, le but final du jeu est d'aider votre faction à remporter la guerre. La faction possédant le score le plus élevé à la fin de la campagne gagne la guerre et remporte la partie.
<br>Chaque lieu stratégique contrôlé par une faction lui rapporte quotidiennement des points en fonction de sa valeur. Dès qu'un lieu stratégique est capturé par la faction adverse, les points changent de camp.
<br>La page de score de campagne renseigne sur le score total accumulé et sur l'évolution du bonus quotidien, ainsi que la répartition entre les différents fronts.
<br>Chaque action de chaque personnage aura une influence sur la réussite de sa faction. Qu'il soit simple pilote, commandant de bataillon, officier d'état-major : tous auront un rôle important à jouer !
<div class='alert alert-danger'>La coopération entre les joueurs et la communication sont indispensables à la réussite.<b>L'Aube des Aigles n'est pas un jeu en solitaire.</b></div>
</div>
