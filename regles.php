<div id='tab_em'>
<h1>6- L'Etat-Major</h1>
<div class='alert alert-info'>Les différentes nations ont besoin de personnes capables de gérer les unités, les infrastructures, d'organiser les missions, la production ou le ravitaillement, afin que leur armée puisse opérer de manière optimale.</div>
<p>Dans le jeu, chaque nation possède plusieurs postes de commandement sur chaque front.
<br>Chaque front possède un <b>Commandant en Chef</b>, fonction qui coordonne toutes les autres, autant l'aérien que le terrestre, le naval, la logistique et le renseignement.
<br>Le commandant en chef peut réaliser toutes les actions de ses subordonnés s'ils sont absents, et il peut en plus :
<br>- Gérer les mutations
<br>- Définir des missions de coordination</p>

<p>Les autres postes sont</p>

<h2>Le commandant de l'armée de terre</h2>
<br>- Il peut consulter les cartes, l'état des dépôts, les rapports et les alertes.
<br>- Il peut contrôler les unités EM terrestres.

<h2>Le commandant de la marine</h2>
<br>- Il peut consulter les cartes, l'état des dépôts, les rapports et les alertes.
<br>- Il peut contrôler les unités EM navales.

<h2>Le commandant de l'armée de l'air</h2>
<br>- Il peut consulter les cartes, l'état des dépôts, les rapports et les alertes.
<br>- Il peut contrôler les unités EM aériennes.

<h2>L'officier de renseignements</h2>
<br>- Il peut consulter les cartes, l'état des dépôts, la production, les infrastructures, les opérations, les rapports et les alertes.
<br>- Il peut effectuer des actions de renseignement.

<h2>L'officier logistique</h2>
<br>- Il peut consulter les cartes, l'état des dépôts, la production.
<br>- Il peut contrôler les unités EM logistiques.

<h2>L'officier des infrastructures</h2>
<br>- Il peut consulter les cartes et l'état des villes et bases.
<br>- Il peut gérer les infrastructures.
<div class='alert alert-info'>Ces fonctions sont accessibles au personnage Officier. Pour accéder à une de ces fonctions, il suffit simplement de poser sa candidature via l'interface du jeu.
<br>il n'est pas nécessaire d'être un expert ou un ancien sur le jeu pour accéder à ces fonctions. Il est cependant nécessaire de posséder du temps à consacrer et une bonne dose de motivation.</div>
