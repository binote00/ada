<h1>4- Personnage</h1>
<div class='alert alert-info'>Vous incarnez un pilote ou un officier de la seconde guerre mondiale. Votre personnage débute la partie en tant que novice, au grade le plus bas.
<br>Vous combattez sous les couleurs allemandes, anglaises, belges, françaises ou italiennes dans un premier temps. Les nations soviétiques, japonaises et américaines sont disponibles en fonction de la date de jeu.</div>
Les capacités du personnage sont définies par différentes caractéristiques :
<h2>Caractéristiques</h2>
<h3>Avancement</h3>
Caractéristique représentant l'avancement de votre personnage pour l'obtention d'un grade supérieur.<br>
<br>Le grade: 
<ul><li>permet d'accéder aux divers postes de commandement (escadrille, état-major, ...).</li>
<li>permet d'accéder à des missions supplémentaires ainsi que plus d'options et de libertés concernant les missions.</li>
<li>influence les actions de gestion.</li>
<li>offre des options supplémentaires de temps libre.</li>
<li>permet d'acquérir un avion personnalisé.</li></ul>
<h3>Réputation</h3>
Caractéristique représentant la célébrité, aussi bien au sein de vos alliés que de vos ennemis. La réputation permet d'obtenir des décorations, et d'autres avantages.<br>
<br>La réputation:
<ul><li>influence en partie l'efficacité des actions de gestion (comme lors de la commande d'avions, de carburant ou de munitions).</li>
<li>influence en partie l'efficacité des formations données à d'autres pilotes.</li>
<li>influence certaines actions de temps libre (comme lors de la réception organisée par le Général ou interroger un prisonnier).</li>
<li>a de l'importance lors de certaines situations de mission (comme lors de l'échange de prisonniers).</li>
<li>a de l'importance lorsque votre pilote postule pour une fonction de staff, ou encore lorsqu'il tente de convaincre un pilote réputé d'être son ailier.</li>
<li>conditionne l'accès à certaines pièces d'équipement.</li>
<li>permet d'acquérir un avion personnalisé.</li></ul>
<h3>Trait</h3>
Le trait représente l'orientation ou la spécialisation acquise lors de la formation de base du personnage.
<h3>Compétences</h3>
Au cours de sa carrière, le personnage peut acquérir diverses compétences. Il n'est pas possible de les acquérir toutes, un choix devra être fait par le joueur.
