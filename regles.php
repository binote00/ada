<h1>3- Le Déroulement du jeu</h1>
La partie débute en mai 1940 et se termine en août 1945. Un jour dans le jeu équivaut à un jour réel, l'Aube des Aigles est un jeu destiné aux joueurs qui aiment les parties de très longue durée où la réflexion et la préparation des décisions a autant d'importance que l'action proprement dit.
<br>Durant chaque campagne, les joueurs auront la possibilité de créer eux-même l'action en gérant l'aspect stratégique ou encore de suivre les consignes de leur état-major en incarnant un rôle plus tactique, afin de faire pencher la balance en faveur de leur faction.
<br>Pour se faire chaque personnage dispose de <a href="#tab_credit">Credit Temps</a> <img src='images/CT40.png'> pour effectuer ses actions chaque jour.
<br><br>Chaque personnage peut participer de différentes manières :
<ul> 
	<li> Le pilote aura à coeur d'effectuer ses missions, en suivant les consignes de son chef d'escadrille. Qu'il soit dans la chasse, le bombardement, la reconnaissance, la patrouille maritime, le transport ou les unités d'assaut, sa faction aura besoin de lui.
	<li> Dès qu'il sera chef d'escadrille il veillera à approvisionner son escadrille en matériel divers afin de lui permettre d'être opérationnelle, tout comme il veillera à l'état des infrastructures de la base. Il gérera les pilotes (IA) de son escadrille et décidera des missions à effectuer.</li> 
	<li> L'officier commandant d'armée jouera un rôle tactique sur la partie terrestre ou navale du front. Il dirigera des unités au combat qui lui seront attribuées par le commandant de front.</li>
	<li> L'officier d'état-major aura à coeur d'analyser la situation d'un point de vue stratégique à différents niveaux, allant de la gestion du ravitaillement ou des infrastructures au commandement d'un front entier.</li> 
</ul> 
<div class='alert alert-danger'>L'Aube des Aigles est un jeu complexe mettant l'accent sur la coopération entre les joueurs. <b>Le débutant à tout intérêt à entrer en contact le plus tôt possible avec les autres joueurs pour être guidé dans le jeu.</b>
<br>Nous vous conseillons également de bien utiliser les boutons d'aide et les infobulles présentes dans le jeu.</div>
